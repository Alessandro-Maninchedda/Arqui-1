				<p>
               ¿Qué problemática queremos resolver con esta solución programada?
               <br><br>
               Pues las instituciones que están bajo la supervisión del Ministerio de Educación Pública, mejor conocido como MEP por sus siglas; no tienen ningún sistema que automatice y ayude a sus maestros y profesores a poder registrar y llevar un conteo de sus “Lecciones acumualadas”. 
               <br><br>
               ¿A qué se refiere con lecciones acumuladas? Para comprender esto es importante primero entender que los horarios de clases constan de lecciones de 40 minutos de duración. En escuelas es más visto que los maestros impartan las 4 materias básicas a un solo grupo de estudiantes, pero también hay lecciones de materias complementarias como Música, Inglés, Religión, Artes Industriales, entre otras… cuando uno de estos profesores de dichos cursos menciones no puede impartir las lecciones determinadas, los profesores guías o los de las 4 básicas se ven en la obligación de cubrir estos 40 minutos si así lo permite su horario de clases. 
               <br><br>
               A esto exactamente se les llama Lecciones Acumuladas, ¿por qué la palabra acumulada? Porque ellos en su sana teoría no tienen que dar esta clase en específico y cuenta como tiempo libre, por lo tanto ellos registran tal lección y la puede acumular o utilizar para otros fines después, como terminar su horario un día de la semana según lo permita la o el director de la institución. 
               <br><br>
               Muchas veces estas lecciones libres los docentes las utilizan para atención de padres de estudiantes, revisar trabajos o exámenes o incluso para hacer registro de expedientes.
               <br><br>
               ¿Por qué decidimos hacer este programa? 
               <br><br>
               El objetivo es facilitar todo este registro de lecciones acumuladas, porque se hace totalmente manual, el proceso consta de una hoja que los profesores rellenan con los datos correspondientes, de la fecha que se acumuló, cantidad de lecciones, día que quieren tomar ventaja de esto y por supuesto nombre y firma de persona que autoriza este cambio de horario, ya sea el director o algún asistente de dirección.

